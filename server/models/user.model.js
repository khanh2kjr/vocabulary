const mongoose = require('mongoose')
const Schema = mongoose.Schema
const UserModelSchema = new Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
})
module.exports = mongoose.model('users', UserModelSchema)
